# Assigning a variable(f_name) to a string
# \t adds a tab and \n changes to a new line
f_name = "\tHamad Noor\n"

# Printing the value of variable(f_name)
print(f_name)

# .lstrip removes the leading characters
# Printing the value without the tab
print(f_name.lstrip())

# .rstrip removes the trailing characters
# Printing the value without the new line
print(f_name.rstrip())

# .strip removes both the leading and trailing characters
# Printing the value without the tab and new line
print(f_name.strip())