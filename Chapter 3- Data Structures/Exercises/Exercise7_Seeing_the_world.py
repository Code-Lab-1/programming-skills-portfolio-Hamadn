# Assigning a variable(places) to the list
places = ['Pakistan', 'Paris', 'Switzerland', 'Chennai', 'Japan']

# Printing the list
print(places)

# Printing the list in the acsending order 
print(sorted(places))
# Printing the original list
print(places)

# Printing the list in the acsending order but in reverse
print(sorted(places, reverse=True))
# Printing the original list
print(places)

# Changes the order of the list
places.reverse()
print(places)

# Changes the order of the list again
places.reverse()
print(places)

# Sorts and prints the list in alphabetical order
places.sort()
print(places)

# Sort the list and reverse the order
places.sort(reverse=True)
print(places)