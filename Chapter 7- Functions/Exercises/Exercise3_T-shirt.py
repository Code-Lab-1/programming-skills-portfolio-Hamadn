# Creating a function called make_shirt with two arguments(size and message)
def make_shirt(size, message):
# Prints a statement using concatenation of string and variable
    print("\nI'm making a " + size + " " + "t-shirt.")
    print("It says, " + message)
# Argument for the size is 'small' and argument for the message is 'I love Python'
make_shirt('small', 'I love Python!')
# Argument for size is 'large and argument for message is 'Hello World'
make_shirt(message="Hello World!", size='large')